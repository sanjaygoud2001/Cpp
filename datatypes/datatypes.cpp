    cout<<"Enter the value of float: ";
    cin>>b;
    cout<<endl;
    char c;
    cout<<"Enter the value of char: ";
    cin>>c;
    cout<<endl;
    bool d;
    cout<<"Enter the value of bool: ";
    cin>>d;
    cout<<endl;
